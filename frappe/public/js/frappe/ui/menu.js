import "../dom";
nts.provide("nts.ui");

nts.ui.menu = class ContextMenu {
	constructor(opts) {
		this.template = $(`<div class="nts-menu context-menu" role="menu"></div>`);
		this.menu_items = opts.menu_items;
		this.name = nts.utils.get_random(5);
		this.open_on_left = opts.open_on_left;
		this.size = opts.size;
		this.opts = opts;
		Object.assign(this, opts);
		this.nested_menus = [];
		this.setup_menu_toggle();
	}
	setup_menu_toggle() {
		const me = this;
		if (this.opts.right_click) {
			$(this.opts.parent).on("contextmenu", function (event) {
				event.preventDefault();
				event.stopPropagation();
				if (me.visible) {
					me.hide();
					me.opts.onHide && me.opts.onHide(me.parent);
				} else {
					me.show(event);
					me.opts.onShow && me.opts.onShow(me.parent);
				}
			});
		} else {
			$(this.opts.parent).on("click", function (event) {
				event.preventDefault();
				event.stopPropagation();
				if (!me.parent_menu) {
					if (me.visible) {
						me.hide();
						me.opts.onHide && me.opts.onHide(me.parent);
					} else {
						me.show(event);
						me.opts.onShow && me.opts.onShow(me.parent);
					}
				}
			});
		}
	}
	make() {
		this.template.empty();
		this.menu_items_to_show = [];
		this.menu_items.forEach((f) => {
			f.condition =
				f.condition ||
				function () {
					return true;
				};
			if (f.condition()) {
				this.add_menu_item(f);
				this.menu_items_to_show.push(f);
			}
		});

		// if (!$.contains(document.body, this.template[0])) {
		// 	$(document.body).append(this.template);
		// }

		// only append if there are items to show
		if (this.menu_items_to_show.length > 0) {
			$(document.body).append(this.template);
		}

		this.set_styles();
	}
	set_styles() {
		if (this.size) {
			this.template.css({
				width: this.size,
			});
		}
	}
	add_menu_item(item) {
		const me = this;
		item.nested_menus = [];
		let item_wrapper = $(
			`<div class="dropdown-menu-item"><div class="dropdown-divider documentation-links"></div></div>`
		);
		if (item?.is_divider) {
			item_wrapper = $(
				`<div class="dropdown-menu-item"><div class="dropdown-divider documentation-links"></div></div>`
			);
		} else {
			const iconMarkup = item.icon_url
				? `<img class="logo" src="${item.icon_url}">`
				: item.icon_html
				? item.icon_html
				: item.icon
				? nts.utils.icon(item.icon)
				: "";
			let chevron_direction = nts.utils.is_rtl() ? "left " : "right";
			item_wrapper = $(`<div class="dropdown-menu-item" onclick="${
				item.action ? `return ${item.action}` : ""
			}">
				<a>
					<div class="menu-item-icon" ${!(iconMarkup != "") ? "hidden" : ""}>
						${iconMarkup}
					</div>
					<span class="menu-item-title">${__(item.label)}</span>
					<div class="menu-item-icon" style="margin-left:auto">
						${item.items && item.items.length ? nts.utils.icon(`chevron-${chevron_direction}`) : ""}
					</div>

				</a>
			</div>`);
			if (!item.url) {
				item_wrapper.on("click", function (event) {
					item.onClick && item.onClick();
					if (!(item.items && item.items.length)) {
						me.opts.onItemClick && me.opts.onItemClick(me.opts.parent);
						me.hide();
					} else {
						if (!me.current_menu) {
							me.nested_menus.forEach((menu) => {
								if (menu.parent.get(0) == this) {
									me.current_menu = menu;
								}
							});
							me.current_menu.show(event);
						} else {
							if (me.current_menu.parent.get(0) == this) {
								// this ensures toggling would work on nested item's parent
								me.current_menu.hide();
								me.current_menu = null;
							} else {
								// this ensures the other nested item would close before opening the next one
								me.current_menu.hide();
								me.nested_menus.forEach((menu) => {
									if (menu.parent.get(0) == this) {
										me.current_menu = menu;
									}
								});
								me.current_menu.show();
							}
						}
					}
				});
			} else if (item.items) {
				$();
			} else {
				item_wrapper.on("click", function () {
					me.nested_menus.forEach((menu) => {
						menu.hide();
					});
					me.hide();
					me.opts.onHide && me.opts.onHide(me);
					if (item.url.startsWith("/desk")) {
						nts.set_route(item.url);
					} else if (item.url.startsWith("/")) {
						window.location.href = window.location.origin + item.url;
					} else {
						window.open(item.url, "_blank").focus();
					}
				});
			}
		}
		item_wrapper.appendTo(this.template);
		if (item.items) {
			let nested_menu = this.handle_nested_menu(item_wrapper, item);
			this.nested_menus.push(nested_menu);
		}
	}

	handle_nested_menu(item_wrapper, item) {
		return nts.ui.create_menu({
			parent: item_wrapper,
			menu_items: item.items,
			nested: true,
			parent_data: item,
			parent_menu: this.name,
		});
	}

	show(event) {
		this.make();
		const offset = $(this.parent).offset();
		const height = $(this.parent).outerHeight();
		this.left_offset = 0;
		this.gap = 4;
		if (this.opts.nested && this.opts.parent_menu) {
			let top =
				this.parent.get(0).getBoundingClientRect().bottom -
				this.parent.get(0).getBoundingClientRect().height;
			let dropdown = nts.menu_map[this.opts.parent_menu].template;
			let width = dropdown.outerWidth();
			let offset = $(dropdown).offset();
			let left = offset.left;
			if (nts.utils.is_rtl()) {
				left = left - width - this.gap;
			} else {
				left = left + width + this.gap;
			}
			this.template.css({
				display: "block",
				position: "absolute",
				top: top + "px",
				left: left + "px",
			});
		} else {
			this.template.css({
				display: "block",
				position: "absolute",
				top: offset.top + height + this.gap + "px",
				left: offset.left,
			});
		}

		if (this.open_on_left) {
			this.left_offset = this.parent.get(0).getBoundingClientRect().width;
			this.template.css({
				left:
					offset.left -
					this.template.get(0).getBoundingClientRect().width +
					this.left_offset +
					"px",
			});
		}

		if (this.opts.right_click) {
			this.template.css({
				left: `${event.clientX}px`,
				top: `${event.clientY}px`,
			});
		}

		this.visible = true;
		nts.visible_menus.push(this);
	}
	close_all_other_menu() {
		$(".context-menu").hide();
	}
	hide() {
		this.template.css("display", "none");
		this.visible = false;
	}
	mouseX(evt) {
		if (evt.pageX) {
			return evt.pageX;
		} else if (evt.clientX) {
			return (
				evt.clientX +
				(document.documentElement.scrollLeft
					? document.documentElement.scrollLeft
					: document.body.scrollLeft)
			);
		} else {
			return null;
		}
	}

	mouseY(evt) {
		if (evt.pageY) {
			return evt.pageY;
		} else if (evt.clientY) {
			return (
				evt.clientY +
				(document.documentElement.scrollTop
					? document.documentElement.scrollTop
					: document.body.scrollTop)
			);
		} else {
			return null;
		}
	}
};

nts.menu_map = {};
nts.visible_menus = [];

nts.ui.create_menu = function (opts) {
	if (!opts.right_click) $(opts.parent).css("cursor", "pointer");
	let context_menu = new nts.ui.menu(opts);

	nts.menu_map[context_menu.name] = context_menu;

	$(document).on("click", function () {
		if (nts.menu_map[context_menu.name].visible) {
			nts.menu_map[context_menu.name].hide();
			opts.onHide && opts.onHide(opts.parent);
		}
	});

	$(document).on("keydown", function (e) {
		if (e.key === "Escape" && nts.menu_map[context_menu.name].visible) {
			nts.menu_map[context_menu.name].hide();
			opts.onHide && opts.onHide(opts.parent);
		}
	});
	return context_menu;
};

function close_all_open_menus() {
	nts.visible_menus.forEach((menu) => {
		menu.hide();
	});
	nts.visible_menus = [];
}

nts.router.on("change", function () {
	close_all_open_menus();
});
